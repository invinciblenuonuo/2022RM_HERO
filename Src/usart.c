void usart1_report_imu(float num1,float num2,float num3,long num4,long num5)
{
	char tbuf[12]; 
	
	tbuf[0]=((int)num1>>24)&0XFF;
	tbuf[1]=((int)num1>>16)&0XFF;
	tbuf[2]=((int)num1>>8)&0XFF;
	tbuf[3]=(int)num1&0XFF;
	
	tbuf[4]=((int)num2>>24)&0XFF;
	tbuf[5]=((int)num2>>16)&0XFF;
	tbuf[6]=((int)num2>>8)&0XFF;
	tbuf[7]=(int)num2&0XFF;

	tbuf[8]=((int)num3>>24)&0XFF;
	tbuf[9]=((int)num3>>16)&0XFF;
	tbuf[10]=((int)num3>>8)&0XFF;
	tbuf[11]=(int)num3&0XFF;
	
	usart1_niming_report(0XF1,tbuf,12);//飞控显示帧,0XAF
}   


